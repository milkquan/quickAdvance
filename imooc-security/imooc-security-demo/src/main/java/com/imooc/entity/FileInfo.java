package com.imooc.entity;

public class FileInfo {
	
	private String path;

	
	public FileInfo(String path) {
		super();
		this.path = path;
	}

	public FileInfo() {
		super();
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
	
	
}
